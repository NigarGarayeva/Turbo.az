function barterKredit() {
    let barter = document.querySelectorAll('[id*="Btrue"]')
    for (let j = 0; j < barter.length; j++) {
        barter[j].innerHTML += `<i class="fas fa-repeat"></i>`
    }

    let kredit = document.querySelectorAll('[id*="Ktrue"]')
    for (let i = 0; i < kredit.length; i++) {
        kredit[i].innerHTML += `<i class="fas fa-percent"></i>`
    }
}

function checkStyle() {

    if (barterCheck.checked == true) {
        divBarterCheck.style.backgroundColor = "rgba(255, 0, 0, 0.2)"
        divBarterCheck.style.color = "red"
        divBarterCheck.style.border = "1px solid red"
    } else if (barterCheck.checked == false) {
        divBarterCheck.style.backgroundColor = "white"
        divBarterCheck.style.color = "initial"
        divBarterCheck.style.border = "1px solid rgba(178, 178, 178, 0.333)"
    }

    if (kreditCheck.checked == true) {
        divKreditCheck.style.backgroundColor = "rgba(255, 0, 0, 0.2)"
        divKreditCheck.style.color = "red"
        divKreditCheck.style.border = "1px solid red"
    } else if (kreditCheck.checked == false) {
        divKreditCheck.style.backgroundColor = "white"
        divKreditCheck.style.color = "initial"
        divKreditCheck.style.border = "1px solid rgba(178, 178, 178, 0.333)"
    }
}


function like(thisId) {
    id = document.getElementById(thisId)
    if (id.style.color != "red") {
        id.style.color = "red"
    } else {
        id.style.color = "white"
    }
}

